#include <stdio.h>

// #define : 전처리기 매크로(Macro)
// 코드가 진짜로 컴파일(기계어로 번역) 되기 직전에, 
// 지정된 단어를 특정한 숫자나 코드 조각으로 단순 치환(바꿔치기) 해주는 기능입니다.

// 1. 매크로 상수 (상수 정의)
// 코드에서 변하지 않는 고정값을 이름을 붙여서 사용할 때 좋습니다.
// 일반 변수와 구분하기 위해 주로 전체를 대문자로 작성하는 것이 관례입니다.
#define PI 3.14
#define MAX_HP 1000
#define GAME_NAME "Awesome Game"

// 2. 매크로 함수 (수식 정의)
// 간단한 연산을 함수처럼 만들어서 사용할 수 있게 해줍니다.
// 주의점: 진짜 함수가 아니라 '글자 바꿔치기'이기 때문에, 오작동을 막으려면 괄호를 철저하게 쳐야 합니다.
#define SQUARE(x) ((x) * (x))
#define MAX(a, b) ((a) > (b) ? (a) : (b))

int main() {
    printf("--- 1. 매크로 상수 사용해보기 ---\n");
    // 아래 코드는 컴파일 전에 "Awesome Game"과 1000으로 교체됩니다.
    printf("게임 이름: %s\n", GAME_NAME);
    printf("플레이어 최대 체력: %d\n", MAX_HP);
    
    int radius = 5;
    // PI 글자도 3.14로 교체됩니다.
    double area = radius * radius * PI;
    printf("반지름이 %d인 원의 넓이: %f\n", radius, area);
    

    printf("\n--- 2. 매크로 함수 사용해보기 ---\n");
    int num = 5;
    
    // SQUARE(5) 가 ((5) * (5)) 로 글자 그대로 교체되어 계산됩니다.
    printf("%d의 제곱: %d\n", num, SQUARE(num));
    
    // MAX(10, 20) 가 ((10) > (20) ? (10) : (20)) 로 교체됩니다.
    printf("10과 20중 더 큰 수: %d\n", MAX(10, 20));


    // ※ 매크로 함수는 성능상 아주 미세한 이점이 있지만, 단순 '글자 바꿔치기'의 특성상 
    // 예기치 못한 논리적 오류를 발생시키기 쉽습니다. 
    // 따라서 복잡한 로직은 매크로 대신 진짜 '함수'를 사용하는 것이 안전합니다.

    return 0;
}
